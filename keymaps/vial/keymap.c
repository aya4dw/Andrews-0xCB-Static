/* RLE decode shifted: each page's content shifts right `shift` cols */
static void decode_cat_frame(unsigned char const *frame, uint8_t shift) {
    uint16_t cursor = 0;
    uint8_t i = 1, size = pgm_read_byte(frame);

    while (i < size && cursor < 512) {
        uint8_t count = pgm_read_byte(frame + i); i++;
        if (count & 0x80) {
            count &= ~(0x80);
            for (uint8_t uniqs = 0; uniqs < count && cursor < 512; ++uniqs) {
                uint8_t byte = pgm_read_byte(frame + i); i++;
                uint8_t page = cursor / 128;
                uint8_t col  = cursor % 128;
                if (col + shift < 128)
                    oled_write_raw_byte(byte, page * 128 + col + shift);
                cursor++;
            }
        } else {
            uint8_t byte = pgm_read_byte(frame + i); i++;
            for (uint8_t reps = 0; reps < count && cursor < 512; ++reps) {
                uint8_t page = cursor / 128;
                uint8_t col  = cursor % 128;
                if (col + shift < 128)
                    oled_write_raw_byte(byte, page * 128 + col + shift);
                cursor++;
            }
        }
    }
    /* clear left `shift` columns on each page */
    for (uint8_t p = 0; p < 4; p++)
        for (uint8_t c = 0; c < shift && c < 128; c++)
            oled_write_raw_byte(0, p * 128 + c);
}

static void animate_cat(uint32_t interval, uint8_t shift) {
    static uint8_t tap_index = 0, idle_index = 0;

    if (interval < CAT_TAP_INTERVAL) {
        tap_index = (tap_index + 1) & 1;
        decode_cat_frame(tap[tap_index], shift);
    } else if (interval < CAT_PAWS_INTERVAL) {
        decode_cat_frame(paws, shift);
    } else {
        idle_index = idle_index < CAT_IDLE_FRAMES - 1 ? idle_index + 1 : 0;
        decode_cat_frame(idle[idle_index], shift);
    }
}

#define CAT_SHIFT 30

static void render_bongocat(void) {
    static uint16_t frame_timer = 0;
    uint32_t  const input_timer = last_matrix_activity_time();

    if (timer_elapsed(frame_timer) > CAT_FRAME_DURATION) {
        frame_timer = timer_read();
        animate_cat(timer_elapsed32(input_timer), CAT_SHIFT);
    }
}

bool oled_task_user(void) {
    static bool finished_timer = false;
    if (!finished_timer && (timer_elapsed(startup_timer) < 3000)) {
        render_logo();
    } else {
        if (!finished_timer) {
            oled_clear();
            finished_timer = true;
        }
        render_bongocat();
        render_layer();
    }
    return false;
}
